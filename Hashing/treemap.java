import java.util.TreeMap;

public class treemap {
    public static void main(String[] args) {
        TreeMap<Integer,String> tm = new TreeMap<>();
        tm.put(8,"Chinmay");
        tm.put(7, "Ram");
        tm.put(11,"Krishna");
        tm.put(1,"Vishnu");
        System.out.println(tm);
    }
}
